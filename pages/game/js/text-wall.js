/* ============================================
   文字立墙配置
   修改以下配置可以自定义文字内容、位置、样式
   ============================================ */
export const TextWallConfig = {
    // 📝 文字内容（修改这里可以改变显示的文字）
    WORD: ['A', 'S', 'T', 'R', 'B', 'O', 'T'],

    // 📐 尺寸参数
    LETTER_SIZE: 9,         // 每个字母的像素尺寸 (9x9)
    GAP: 1,                 // 字母之间的间隔格数

    // 🗺️ 位置参数
    GROUND_Y: 18,           // 沙质地面 Y 坐标
    FLAT_RADIUS_X: 40,      // 平地 X 方向半径
    FLAT_RADIUS_Z: 10,      // 平地 Z 方向半径
    WALL_Z: 0,              // 立墙中心 Z 坐标
    WALL_DEPTH: 3,          // 立墙厚度（格数，中心对称）

    // 🎨 样式参数
    WALL_BLOCK_TYPE: 'LEAVES',  // 立墙方块类型（使用 BlockType 的属性名）
    GROUND_BLOCK_TYPE: 'GRASS', // 地面方块类型（文字区域外的正常地面）
    UNDERGROUND_TYPES: {       // 地下方块层配置
        deep: 'STONE',           // 深层
        shallow: 'DIRT',         // 浅层（地面下5层）
    },
};

/* ============================================
   TextWall 类 - 文字立墙生成器
   ============================================ */
export class TextWall {
    constructor() {
        this._calculateDimensions();
    }

    /** 根据配置计算文字总尺寸 */
    _calculateDimensions() {
        const { WORD, LETTER_SIZE, GAP } = TextWallConfig;
        this.totalWidth = WORD.length * LETTER_SIZE + (WORD.length - 1) * GAP;
        this.startX = -Math.floor(this.totalWidth / 2);
        this.baseY = TextWallConfig.GROUND_Y + 1;
    }

    /** 获取立墙使用的方块类型 */
    getWallBlockType() {
        return BlockType[TextWallConfig.WALL_BLOCK_TYPE] || BlockType.LEAVES;
    }

    /** 获取地面高度 */
    getGroundY() {
        return TextWallConfig.GROUND_Y;
    }

    /** 判断区块是否在文字区域内 */
    isInTextZone(cx, cz, chunkSize) {
        const x0 = cx * chunkSize;
        const x1 = x0 + chunkSize - 1;
        const z0 = cz * chunkSize;
        const z1 = z0 + chunkSize - 1;
        const RX = TextWallConfig.FLAT_RADIUS_X;
        const RZ = TextWallConfig.FLAT_RADIUS_Z;
        return x1 >= -RX && x0 <= RX && z1 >= -RZ && z0 <= RZ;
    }

    /** 获取立墙文字方块（XY 平面） */
    getTextBlock(wx, wy, wz) {
        const wallMinZ = TextWallConfig.WALL_Z - Math.floor(TextWallConfig.WALL_DEPTH / 2);
        const wallMaxZ = TextWallConfig.WALL_Z + Math.floor(TextWallConfig.WALL_DEPTH / 2);

        // Z 范围检查
        if (wz < wallMinZ || wz > wallMaxZ) return BlockType.AIR;

        // Y 范围检查
        const maxY = this.baseY + TextWallConfig.LETTER_SIZE - 1;
        if (wy < this.baseY || wy > maxY) return BlockType.AIR;

        // X 坐标转换为文字局部坐标
        const lx = wx - this.startX;
        const ly = wy - this.baseY;

        if (lx < 0 || ly < 0 || ly >= TextWallConfig.LETTER_SIZE) return BlockType.AIR;

        // 查找对应字母
        let offset = 0;
        for (const ch of TextWallConfig.WORD) {
            if (lx >= offset && lx < offset + TextWallConfig.LETTER_SIZE) {
                const letterData = LETTERS[ch];
                if (!letterData) return BlockType.AIR;
                return letterData[TextWallConfig.LETTER_SIZE - 1 - ly][lx - offset] ? this.getWallBlockType() : BlockType.AIR;
            }
            offset += TextWallConfig.LETTER_SIZE + TextWallConfig.GAP;
        }

        return BlockType.AIR;
    }

    /** 生成文字区域的区块数据 */
    generateTextZoneChunk(chunk, chunkSize, chunkHeight) {
        const { cx, cz } = chunk;
        const wx0 = cx * chunkSize;
        const wz0 = cz * chunkSize;
        const GY = TextWallConfig.GROUND_Y;

        for (let lz = 0; lz < chunkSize; lz++) {
            for (let lx = 0; lx < chunkSize; lx++) {
                const wx = wx0 + lx;
                const wz = wz0 + lz;

                for (let y = 0; y < chunkHeight; y++) {
                    let blockType;

                    // 地下层
                    if (y < GY - 5) {
                        blockType = BlockType[TextWallConfig.UNDERGROUND_TYPES.deep];
                    } else if (y < GY) {
                        blockType = BlockType[TextWallConfig.UNDERGROUND_TYPES.shallow];
                    } else if (y === GY) {
                        // 地面层（文字区域的地面用泥土，让立墙更突出）
                        blockType = BlockType.DIRT;
                    } else if (y >= this.baseY && y <= this.baseY + TextWallConfig.LETTER_SIZE - 1) {
                        // 文字立墙层
                        blockType = this.getTextBlock(wx, y, wz);
                    } else {
                        blockType = BlockType.AIR;
                    }

                    chunk.setBlock(lx, y, lz, blockType);
                }
            }
        }
    }
}

